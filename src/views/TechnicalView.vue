        <p>网站安全防护：</p>
        <p>
          1、物理安全：包括环境安全、设备安全、人员的访问控制、审计记录、异常情况的追查等。
          我公司在保障网络物理安全方面，采取如下措施：
        </p>
        <p>（1）机房安全管理措施：</p>
