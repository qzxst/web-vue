        </p>
        <p>
          2、网络安全：包括网络拓扑结构、网络设备的管理、网络安全访问措施（防火墙、入侵检测系统、VPN等）、安全扫描、远程访问、不同级别网络的访问控制方式、识别/认证机制等。
          我公司建立了包括网络拓扑结构、网络设备的管理、网络安全访问措施（防火墙、入侵检测系统、VPN等）、安全扫描、远程访问、不同级别网络的访问控制方式、识别/认证机制等，旨在防范和抵御网络可能受到的攻击，保证网络资源不被非法使用和访问，保护网内流转的数据安全。
        </p>
