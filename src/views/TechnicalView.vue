          除网络管理员外，任何人不得擅自进入机房，不得擅自接触机房设备。
          不得擅自改动或移动机房内的电源、空调及机柜、服务器、交换机等计算机、网络设备。
        </p>
        <p>未经许可不得随意允许非机房内工作人员进入机房。</p>
        <p>未经批准不能带外人参观、演示、查询信息等网上操作</p>
        <font _mstmutation="1">。</font>
        <p>严禁带火种进入机房。 如发现机房内有烟雾甚至火焰，立即切断电源。</p>
        <p>下班时，检查设备的运转情况，并填写日志记录。</p>
        <p>（2）系统口令的安全管理：</p>
        <p>严格控制知道密码的人数。</p>
        <p>
          网络管理员、系统管理员调离岗位后一小时内由上级监督检查更换新的密码。
        </p>
        <p>
          对网络管理员、系统管理员和系统操作员所用口令每十五天更换一次，重要口令要多于八位。
        </p>
        <p>口令要无规则，防止口令被猜出。</p>
        <p>员工离职后，其所有账号和口令应立即从相应PASSWORD文件中清除。</p>
        <p>
          调试人员调试维护完成后一小时内，由系统管理员删除其所用账号和密码。
        </p>
        <p>（3）完善的系统备份计划</p>
        <p>
          我公司的策略是以一个月为一个周期，一个月进行一次完全备份，每天晚上进行这一天改变的数据备份，即增量备份。
          这样一旦发生数据丢失，首先恢复前一个完全备份，然后按日期一个一个恢复每天的备份，就能恢复到前一天的情况。
          这样做虽然工作量要大，但数据丢失的风险最小。
        </p>
        <p>
          备份完成后，还定期地检验备份数据的有效性。
          避免类似到急需恢复数据时才发现数据损坏等情况的发生，确保数据万无一失。
        </p>
